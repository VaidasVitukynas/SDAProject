package lt.sdacademy.Fundamentals.object_oriented.Bicycle.room;

public class Room {
    private int width;
    private int lenght;

    public Room(int with, int lenght) {
        this.width = with;
        this.lenght = lenght;
    }

    public int getWith() {
        return width;
    }

    public int getLenght() {
        return lenght;
    }

    public int getWidth() {
        return width;
    }
}

